import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..', '..');
const sourceDir = path.join(rootDir, 'yorumlar');
const outputFile = path.join(rootDir, 'assets', 'js', 'template-data.js');

const entries = fs
  .readdirSync(sourceDir)
  .filter((file) => file.endsWith('.json'))
  .sort();

const payload = {};

for (const fileName of entries) {
  const fullPath = path.join(sourceDir, fileName);
  const key = path.basename(fileName, '.json');
  const fileContent = fs.readFileSync(fullPath, 'utf8');
  payload[key] = JSON.parse(fileContent);
}

const banner = '// This file is auto-generated by scripts/core/build-template-data.mjs\n';
const body = `window.__KARNE_TEMPLATE_DATA__ = ${JSON.stringify(payload, null, 2)};\n`;

fs.writeFileSync(outputFile, banner + body, 'utf8');
console.log(`Template data written to ${outputFile}`);
